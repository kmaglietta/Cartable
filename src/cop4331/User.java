package cop4331;

public class User {
	private int id;
	private String username;
	private String firstName;
	private String lastName;
	
	public User() {
		
	}
	
	public int getId() {
		return id;
	}
	
	public String username() {
		return username;
	}
	
	public String firstName() {
		return firstName;
	}
	
	public String lastName() {
		return lastName;
	}
}
